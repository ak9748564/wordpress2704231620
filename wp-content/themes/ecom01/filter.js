var li = document.getElementsByClassName("li");
var content = document.getElementsByClassName("content");
var bi_chevron_up = document.getElementsByClassName("bi-chevron-up");

li[0].onclick = function() {
    if(content[0].style.height == "100%") 
    {
        content[0].style.height = "24px";
        content[0].style.transition = "height 0.4s ease-in-out";
        bi_chevron_up[0].style.transform = "rotate(180deg)";
        bi_chevron_up[0].style.transition = "transform 0.4s ease-in-out";
    } 
    else 
    {
        content[0].style.height = "100%";
        content[0].style.transition = "height 0.4s ease-in-out";
        bi_chevron_up[0].style.transform = "rotate(0deg)";
        bi_chevron_up[0].style.transition = "transform 0.4s ease-in-out";
    }
}
